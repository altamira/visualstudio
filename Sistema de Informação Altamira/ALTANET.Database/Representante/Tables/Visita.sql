    [Data e Hora da Visita]          DATETIME       NOT NULL,
    [Identificador da Sessão]        INT            CONSTRAINT [DF_Visita_Identificador da Sessão] DEFAULT ((0)) NOT NULL,
    [Identificador do Representante] INT            CONSTRAINT [DF_Visita_Identificador do Representante] DEFAULT ((0)) NOT NULL,
    [Código do Representante]        CHAR (3)       NOT NULL,
    [Identificador do Recado]        INT            CONSTRAINT [DF_Agenda_Identificador do Recado] DEFAULT ((0)) NOT NULL,
    [Número do Orçamento]            INT            CONSTRAINT [DF_Agenda_Número do Orçamento] DEFAULT ((0)) NOT NULL,
    [Nome Fantasia]                  NVARCHAR (20)  NULL,
    [Razão Social]                   NVARCHAR (50)  NULL,
    [Endereço]                       NVARCHAR (50)  NULL,
