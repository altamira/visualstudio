    [Cidade]                         NVARCHAR (50)  NULL,
    [Estado]                         NCHAR (2)      NULL,
    [Nome do Contato]                NVARCHAR (40)  NULL,
    [Departamento]                   NVARCHAR (35)  NULL,
    [DDD do Telefone]                NCHAR (3)      NULL,
    [Telefone]                       NVARCHAR (15)  NULL,
    [Ramal do Telefone]              NCHAR (5)      NULL,
    [DDD do Celular]                 NCHAR (3)      NULL,
    [Celular]                        NVARCHAR (15)  NULL,
    [Email]                          NVARCHAR (100) NULL,
    [Observações]                    NTEXT          NULL,
    CONSTRAINT [PK_Agenda] PRIMARY KEY CLUSTERED ([Número] ASC),
    CONSTRAINT [FK_Visita_Representante] FOREIGN KEY ([Identificador do Representante]) REFERENCES [Representante].[Representante] ([Identificador]),
