	GPIMAC_ALTAMIRA.dbo.CAPCL WITH (NOLOCK) INNER JOIN
	GPIMAC_ALTAMIRA.dbo.ATD WITH (NOLOCK) ON GPIMAC_ALTAMIRA.dbo.CAPCL.Pc0Cod = GPIMAC_ALTAMIRA.dbo.ATD.Atd0Pc0Cod INNER JOIN
	GPIMAC_ALTAMIRA.dbo.CAPCL1 WITH (NOLOCK) ON GPIMAC_ALTAMIRA.dbo.ATD.Atd0Pc1Cod = GPIMAC_ALTAMIRA.dbo.CAPCL1.Pc1Cod AND GPIMAC_ALTAMIRA.dbo.ATD.Atd0Pc0Cod = GPIMAC_ALTAMIRA.dbo.CAPCL1.Pc0Cod INNER JOIN
	GPIMAC_ALTAMIRA.dbo.CASTA WITH (NOLOCK) ON GPIMAC_ALTAMIRA.dbo.ATD.Atd0StA0Cod = GPIMAC_ALTAMIRA.dbo.CASTA.CSta0Cod INNER JOIN
	GPIMAC_ALTAMIRA.dbo.CATPA WITH (NOLOCK) ON GPIMAC_ALTAMIRA.dbo.ATD.Atd0TpA0Cod = GPIMAC_ALTAMIRA.dbo.CATPA.CTpA0Cod INNER JOIN
	GPIMAC_ALTAMIRA.dbo.CAMDA WITH (NOLOCK) ON GPIMAC_ALTAMIRA.dbo.ATD.Atd0MdA0Cod = GPIMAC_ALTAMIRA.dbo.CAMDA.CMdA0Cod INNER JOIN
	GPIMAC_ALTAMIRA.dbo.CAREP WITH (NOLOCK) ON GPIMAC_ALTAMIRA.dbo.ATD.Atd0CvCod = GPIMAC_ALTAMIRA.dbo.CAREP.CVCOD


